package exercises.week2;

public class Counting {

    public static void main(String[] args) {

        System.out.println("Hello World!");

        System.out.println("Teller til ti");

        /*

        Starts from 1 and adds on one number each time
        until it reaches 10

        */

        for (int i = 1; i <= 10; i++) {

            System.out.println(i);
        }

        System.out.println("Petra teller til tjue");

        /*

        Starts from 11 and adds on one number each time
        until it reaches 20

        */

        for (int i = 11; i <= 20; i++) {

            System.out.println(i);
        }
    }
}
